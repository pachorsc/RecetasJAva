    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nuevoNom = Nuevo_Nom.getText();
        String receta = Cambio_recet.getText();

        if (nuevoNom.isEmpty() || receta.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Algun espacio est√° vacio",
                "Error",
                JOptionPane.ERROR_MESSAGE);
        }
        else {
            try {
                BD.basedatos(BD.update("recetas", "nombre", nuevoNom , FinalProject.getRece().getNombre()));
                BD.basedatos(BD.update("recetas", "pasos", receta , FinalProject.getRece().getPasos()));

                Resetas_Usuario V4 = new Resetas_Usuario();
                V4.setVisible(true);
                V4.setLocationRelativeTo(null);
                this.setVisible(false);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Editar_Receta.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(Editar_Receta.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

